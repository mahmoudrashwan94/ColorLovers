package Helpers;

public class ApiHelper {

	/**
     * This method for comparing two numbers 
     * @param firstNumber 
     * @param secondNumber
     * @return true if the first number is greater than the second number
     */
    public static boolean compareTwoIntegers(int firstNumber, int secondNumber) {

        if (firstNumber > secondNumber)
            return true;
        else
            return false;

    }

}
